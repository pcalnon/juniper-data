# Determine Host OS
#####################################################################################################################################################################################################
# shellcheck disable=SC2155
if [[ -f /etc/os-release ]]; then
    export CURRENT_OS="$(grep -e "^NAME=" /etc/os-release | awk -F "\"" '{print $2;}')"
elif [[ "$(uname)" == "Darwin" ]]; then
    export CURRENT_OS="MacOS"
else
    export CURRENT_OS="$(uname -s)"
fi
echo "${CURRENT_OS}"
